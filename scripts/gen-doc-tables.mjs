#!/usr/bin/env node
/**
 * gen-doc-tables.mjs — Dynamic doc table generator
 *
 * Generates/updates documentation sections that would otherwise go stale
 * when actions, tools, or handler files change. Uses:
 *   - src/generated/manifest.json  — tool names, action counts, action lists (fully auto)
 *   - scripts/handler-metadata.json — handler class names, file paths, key services (hand-authored, rarely changes)
 *   - fs.statSync(file).size       — file line counts (auto, live)
 *
 * Targets:
 *   1. docs/development/ACTION_REGISTRY.md   — fully generated (all sections replaced)
 *   2. docs/development/CODEBASE_CONTEXT.md  — fence-marked sections replaced:
 *        <!-- BEGIN_GENERATED:handler-table-base -->
 *        <!-- BEGIN_GENERATED:handler-table-standalone -->
 *
 * Usage:
 *   node scripts/gen-doc-tables.mjs          # Generate (writes files)
 *   node scripts/gen-doc-tables.mjs --check  # Diff only (exit 1 if stale)
 *
 * Wire into: npm run schema:commit (already done via package.json)
 */

import { readFileSync, writeFileSync, statSync } from 'fs';
import { join, relative } from 'path';
import { execSync } from 'child_process';

const ROOT = decodeURIComponent(new URL('..', import.meta.url).pathname).replace(/\/$/, '');
const CHECK_ONLY = process.argv.includes('--check');

// --- Load data sources ---
const manifest = JSON.parse(readFileSync(join(ROOT, 'src/generated/manifest.json'), 'utf8'));
const handlerMeta = JSON.parse(readFileSync(join(ROOT, 'scripts/handler-metadata.json'), 'utf8'));

// Build tool → action list map from manifest
const toolActions = {};
for (const tool of manifest.tools) {
  toolActions[tool.name] = tool.actions;
}

// Build tool → action count map from manifest (authoritative)
const toolCounts = {};
for (const tool of manifest.tools) {
  toolCounts[tool.name] = tool.actionCount;
}

// Build tool → handler metadata map
const handlerMap = {};
for (const h of handlerMeta.handlers) {
  handlerMap[h.tool] = h;
}

// --- Get line count for a source file ---
function lineCount(relPath) {
  try {
    const abs = join(ROOT, 'src', relPath);
    const content = readFileSync(abs, 'utf8');
    return content.split('\n').length;
  } catch {
    return null;
  }
}

// --- Format file column with line count ---
function fileCol(relPath) {
  const lines = lineCount(relPath);
  return lines ? `${relPath} (${lines} lines)` : relPath;
}

// ============================================================================
// GENERATOR 1: ACTION_REGISTRY.md
// ============================================================================

function generateActionRegistry() {
  const today = new Date().toISOString().split('T')[0];
  const actionCount = manifest.actionCount;
  const toolCount = manifest.toolCount;

  const lines = [
    '---',
    `title: ServalSheets — Action Registry (${actionCount} Actions, ${toolCount} Tools)`,
    'category: development',
    `last_updated: ${today}`,
    `description: '> Complete action list by tool. Source of truth for counts: src/schemas/action-counts.ts.'`,
    'version: 1.7.0',
    '---',
    '',
    `# ServalSheets — Action Registry (${actionCount} Actions, ${toolCount} Tools)`,
    '',
    '> Complete action list by tool. Source of truth for counts: `src/schemas/action-counts.ts`.',
    '> Load this file only when working on actions, adding features, or verifying action names.',
    `> Auto-generated by \`scripts/gen-doc-tables.mjs\` — DO NOT EDIT MANUALLY. Run \`npm run gen:docs\` to refresh.`,
    '',
  ];

  // Sort: BaseHandler first (alphabetically within group), then Standalone
  const baseHandlers = handlerMeta.handlers.filter(h => h.type === 'BaseHandler');
  const standaloneHandlers = handlerMeta.handlers.filter(h => h.type === 'Standalone');

  for (const group of [baseHandlers, standaloneHandlers]) {
    for (const handler of group) {
      const { tool, type } = handler;
      const actions = toolActions[tool] ?? [];
      const count = toolCounts[tool] ?? actions.length;

      lines.push(`## ${tool} (${count}) — ${type}`);
      lines.push('');

      // Format actions in wrapped lines of ~100 chars
      const actionStr = actions.join(', ');
      if (actionStr.length <= 100) {
        lines.push(`\`${actionStr}\``);
      } else {
        // Wrap into lines
        const chunks = [];
        let current = '';
        for (const action of actions) {
          const next = current ? `${current}, ${action}` : action;
          if (next.length > 95) {
            chunks.push(current);
            current = action;
          } else {
            current = next;
          }
        }
        if (current) chunks.push(current);
        lines.push(chunks.map(c => `\`${c}\``).join(',\n'));
      }

      lines.push('');
    }
  }

  return lines.join('\n');
}

// ============================================================================
// GENERATOR 2: Handler tables for CODEBASE_CONTEXT.md
// ============================================================================

function generateHandlerTableBase() {
  const baseHandlers = handlerMeta.handlers.filter(h => h.type === 'BaseHandler');
  const rows = baseHandlers.map(h => {
    const count = toolCounts[h.tool] ?? '?';
    const filePart = fileCol(h.file);
    return `| ${h.tool.padEnd(18)} | ${h.handlerClass.padEnd(23)} | ${filePart.padEnd(36)} | ${String(count).padEnd(7)} | ${h.keyService.padEnd(34)} |`;
  });

  const header = [
    '| Tool               | Handler Class           | File                                 | Actions | Key Service                        |',
    '| ------------------ | ----------------------- | ------------------------------------ | ------- | ---------------------------------- |',
  ];
  return [...header, ...rows].join('\n');
}

function generateHandlerTableStandalone() {
  const standaloneHandlers = handlerMeta.handlers.filter(h => h.type === 'Standalone');
  const rows = standaloneHandlers.map(h => {
    const count = toolCounts[h.tool] ?? '?';
    const filePart = fileCol(h.file);
    return `| ${h.tool.padEnd(19)} | ${h.handlerClass.padEnd(38)} | ${filePart.padEnd(36)} | ${String(count).padEnd(7)} | ${h.keyService.padEnd(48)} |`;
  });

  const header = [
    '| Tool                | Handler Class/Function                 | File                                 | Actions | Key Service                                        |',
    '| ------------------- | -------------------------------------- | ------------------------------------ | ------- | -------------------------------------------------- |',
  ];
  return [...header, ...rows].join('\n');
}

// ============================================================================
// FENCE MARKER REPLACEMENT
// ============================================================================

function replaceFencedSection(content, markerName, newContent) {
  const startMarker = `<!-- BEGIN_GENERATED:${markerName} -->`;
  const endMarker = `<!-- END_GENERATED:${markerName} -->`;

  const startIdx = content.indexOf(startMarker);
  const endIdx = content.indexOf(endMarker);

  if (startIdx === -1 || endIdx === -1) return { content, changed: false };

  const before = content.substring(0, startIdx + startMarker.length);
  const after = content.substring(endIdx);
  const newSection = `${before}\n${newContent}\n${after}`;

  return { content: newSection, changed: newSection !== content };
}

// ============================================================================
// FULL FILE REPLACEMENT (for ACTION_REGISTRY.md)
// ============================================================================

function updateFile(filePath, newContent, label) {
  let existing;
  try { existing = readFileSync(filePath, 'utf8'); } catch { existing = null; }

  if (existing === newContent) {
    console.log(`  ✓ ${label} — already up to date`);
    return false;
  }

  if (CHECK_ONLY) {
    console.log(`  ⚠️  ${label} — STALE (run npm run gen:docs to fix)`);
    return true; // changed
  }

  writeFileSync(filePath, newContent, 'utf8');
  console.log(`  ✅ ${label} — updated`);
  return true;
}

// ============================================================================
// MAIN
// ============================================================================

let anyChanged = false;

console.log(`gen-doc-tables.mjs — ${CHECK_ONLY ? 'CHECK' : 'GENERATE'} mode`);
console.log(`Source: ${manifest.toolCount} tools, ${manifest.actionCount} actions\n`);

// 1. ACTION_REGISTRY.md — fully generated
const actionRegistryPath = join(ROOT, 'docs/development/ACTION_REGISTRY.md');
const actionRegistryContent = generateActionRegistry();
if (updateFile(actionRegistryPath, actionRegistryContent, 'docs/development/ACTION_REGISTRY.md')) {
  anyChanged = true;
}

// 2. CODEBASE_CONTEXT.md — fence-marked sections
const contextPath = join(ROOT, 'docs/development/CODEBASE_CONTEXT.md');
let contextContent;
try { contextContent = readFileSync(contextPath, 'utf8'); } catch { contextContent = null; }

if (contextContent) {
  let updated = contextContent;
  let contextChanged = false;

  const baseResult = replaceFencedSection(updated, 'handler-table-base', generateHandlerTableBase());
  if (baseResult.changed) { updated = baseResult.content; contextChanged = true; }

  const standaloneResult = replaceFencedSection(updated, 'handler-table-standalone', generateHandlerTableStandalone());
  if (standaloneResult.changed) { updated = standaloneResult.content; contextChanged = true; }

  if (contextChanged || updated !== contextContent) {
    if (updateFile(contextPath, updated, 'docs/development/CODEBASE_CONTEXT.md')) {
      anyChanged = true;
    }
  } else {
    console.log(`  ✓ docs/development/CODEBASE_CONTEXT.md — already up to date`);
  }
} else {
  console.log(`  ⚠️  docs/development/CODEBASE_CONTEXT.md — not found`);
}

console.log('');
if (CHECK_ONLY && anyChanged) {
  console.log('❌ Generated doc sections are stale. Run: npm run gen:docs');
  process.exit(1);
} else if (!anyChanged) {
  console.log('✅ All generated doc sections are up to date');
} else {
  console.log('✅ Generated doc sections updated');
}
